export function getPapersForView(view) {
  if (view === "Southeast Asia") {
    return papers.filter(
      (paper) => paper.scope === "Southeast Asia" || paper.scope === "Theory"
    );
  }

  if (view === "Theory") {
    return papers.filter((paper) => paper.scope === "Theory");
  }

  return papers.filter((paper) => paper.scope === view || paper.country === view);
}

export function getThemeOptions(view = null) {
  const source = view ? getPapersForView(view) : papers;
  return Array.from(new Set(source.flatMap((paper) => paper.themes || []))).sort();
}

export function getPapersByTag(tag, view = null) {
  const source = view ? getPapersForView(view) : papers;

  if (!tag || tag === "All") {
    return source;
  }

  return source.filter((paper) => (paper.themes || []).includes(tag));
}

export function getCorpusStats(view = null) {
  const source = view ? getPapersForView(view) : papers;

  const tagCounts = {};
  source.forEach((paper) => {
    (paper.themes || []).forEach((theme) => {
      tagCounts[theme] = (tagCounts[theme] || 0) + 1;
    });
  });

  return {
    total: source.length,
    journalArticles: source.filter((paper) =>
      String(paper.sourceType).toLowerCase().includes("journal")
    ).length,
    withDoi: source.filter((paper) => paper.doi).length,
    withSource: source.filter((paper) => paper.source).length,
    tagCounts,
  };
}